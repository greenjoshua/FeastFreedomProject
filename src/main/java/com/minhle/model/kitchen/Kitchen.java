package com.minhle.model.kitchen; 
import java.time.LocalDate;
import java.time.LocalTime; 
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAttribute;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;

@DynamoDBTable(tableName = "Kitchens")
public class Kitchen {

	@DynamoDBHashKey(attributeName = "Kitchenid")
	@DynamoDBAutoGeneratedKey
	private String id;
	
	@DynamoDBAttribute(attributeName = "StartTime")
	private   LocalTime starttime; 
	
	@DynamoDBAttribute(attributeName = "EndTime")
	private   LocalTime punchout;
	
	@DynamoDBAttribute(attributeName = "ImageUrl")
	 private String imageUrl;
	
	@DynamoDBAttribute(attributeName = "Menu")
	 private Menu menu;
	
	@DynamoDBAttribute(attributeName = "todayDate")
	private LocalDate punchinoutdate;
	
	
	public LocalTime getStarttime() {
		return starttime;
	}
	
	public void setStarttime(LocalTime starttime) {
		this.starttime = starttime;
	}
	
	public Menu getMenu() {
		return menu;
	}
	
	public void setMenu(Menu menu) {
		this.menu = menu;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}


	 
	public LocalDate getPunchinoutdate() {
		return punchinoutdate;
	}

	public void setPunchinoutdate(LocalDate punchinoutdate) {
		this.punchinoutdate = punchinoutdate;
	}
	 
	public LocalTime getStartime() {
		return starttime;
	}

	public void setStartime(LocalTime punchin) {
		this.starttime = punchin;
	}
	 
	public LocalTime getPunchout() {
		return punchout;
	}

	public void setPunchout(LocalTime punchout) {
		this.punchout = punchout;
	}
	 
	public String getImageUrl() {
		return imageUrl;
	}

	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}
	 

	

	
	
}
