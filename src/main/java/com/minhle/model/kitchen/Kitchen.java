package com.minhle.model.kitchen; 
import java.time.LocalDate;
import java.time.LocalTime;
import java.util.List;

import org.springframework.stereotype.Component;

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAttribute;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;

@DynamoDBTable(tableName = "Kitchens")
@Component
public class Kitchen {

	@DynamoDBHashKey 
	@DynamoDBAutoGeneratedKey
	private String id;
	
	@DynamoDBAttribute(attributeName = "StartTime")
	private   String starttime; 
	
	@DynamoDBAttribute(attributeName = "EndTime")
	private   String punchout;
	
	@DynamoDBAttribute(attributeName = "ImageUrl")
	 private String imageUrl;
	
	@DynamoDBAttribute(attributeName = "Menu")
	 private List<Item> menu;
	
	@DynamoDBAttribute(attributeName = "todayDate")
	private String punchinoutdate;

 

	public String getStarttime() {
		return starttime;
	}

	public void setStarttime(String starttime) {
		this.starttime = starttime;
	}

	public String getPunchout() {
		return punchout;
	}

	public void setPunchout(String punchout) {
		this.punchout = punchout;
	}

	public String getImageUrl() {
		return imageUrl;
	}

	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}

	public List<Item> getMenu() {
		return menu;
	}

	public void setMenu(List<Item> menu) {
		this.menu = menu;
	}

	public String getPunchinoutdate() {
		return punchinoutdate;
	}

	public void setPunchinoutdate(String punchinoutdate) {
		this.punchinoutdate = punchinoutdate;
	}
	
	
 
	

	
	
}
